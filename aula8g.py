# Outras funções para listas

lista_numeros = [5, 2, 7, 3, 1, 9, 5, 7]
menor_numero = min(lista_numeros) # a função retorna o menor número da lista
print(f'O menor número da lista é {menor_numero}')
maior_numero = max(lista_numeros) # a função retorna o maior número da lista
print(f'O maior número da lista é {maior_numero}')

lista_ordenada = sorted(lista_numeros) # cria uma lista em ordem a partir da primeira lista
print(lista_ordenada)
soma_lista = sum(lista_numeros)